tail -n 262 $0 | head -n -1 | base64 --decode> /tmp/cq.mp3

for i in "${CTCSS[@]}"
do
  echo "Playing $i tone"
# Rem the next line, if you dont want to play the sound
  play -q /tmp/cq.mp3 &
  play -q -V1 -c 2 -n synth $sound sin $i
  echo "Waiting $delay sec for response..."
  sleep $delay
done

